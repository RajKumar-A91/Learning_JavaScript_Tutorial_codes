//Operators in javaScript:
console.log("different kinds of operators in JavaScript:")
//1.Arithematic Operators:
var x6=5;
var x7=5;
var c=x6+x7;//similarly -(subtraction), *(multiplication), /(division), %(mod). 
console.log("the value of x6+x7:",c);


//Assignment Operators:
var n=1;//=(assigns right side value to left side variable)
console.log(n);
n+=10;
console.log(n);//n=n+10;
n-=10;
console.log(n);//n=n-10;
n*=10;
console.log(n);//n=n*10;
n/=10;
console.log(n);//n=n/10;
n%=10;
console.log(n);//n=n%10;


//Comparision Operators:
let num5=10;
let num6=98;
console.log(num5==num6);//false
console.log(num5<=num6);//true
console.log(num5>num6);//false
console.log(num5<num6);//true
console.log(num5>=num6);//false
//==, >, <, <=, >=.

//Logical Operators:
console.log("Logical Operators:");
console.log("true&&true:",true&&true);
console.log("true&&false:",true&&false);
console.log("false&&false:",false&&false);
console.log("true||true:",true||true);
console.log("false||false:",false||false);
console.log("false||true:",false||true);
console.log("!true:",!true);
console.log("!false:",!false);
