console.log("Variables in JavaScript:")
//Variables are containers to store data and access or retrieve data when ever required. It is simply name of address of a data.
// var(keyword) name-of-variable(userDefined);
var num1=45;
var num2=65;
console.log("num1+num2:",num1+num2);
console.log("num2-num1:",num2-num1);
console.log("num1-num2:",num1-num2);
console.log("num1*num2:",num1*num2);
console.log("num2/num1:",num2/num1);
console.log("num1/num2:",num1/num2);
console.log("num1%num2:",num1%num2);
console.log("num2%num1:",num2%num1);

//let allows you to declare variables that are limited to the scope of a block statement, or expression on which it is used, unlike the var keyword, which declares a variable globally, or locally to an entire function regardless of block scope.

let x1=5;
